#include "../../inc/libft.h"

char			*c_bitoa(uint64_t nb, uint64_t len)
{
	uint64_t	i;
	char		*str;

	i = 0;
	str = s_new(len + 1);
	while (len--)
	{
		if (b_checknth(nb, i) == 1)
			str[len] = '1';
		else
			str[len] = '0';
		i++;
	}
	return (str);
}

